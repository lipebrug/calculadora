public class Calculadora {

    public int fatA;
    public int fatB;
    public float result;

    public void soma(int fatA, int fatB){
        result = fatA + fatB;
        System.out.println(result);
    }

    public void sub(int fatA, int fatB){
        result = fatA + fatB;
        System.out.println(result);
    }

    public void mult(int fatA, int fatB){
        result = fatA * fatB;
        System.out.println(result);
    }

    public void div(int fatA, int fatB){
        result = fatA / fatB;
        System.out.println(result);
    }

}
